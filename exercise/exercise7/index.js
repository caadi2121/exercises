console.log('before push')
let colors = ['red', 'green', 'blue', 'yellow'];
console.log(colors);

console.log("after push")
colors.push("gray")
console.log(colors)

console.log("befor pop")
console.log(colors.length)
colors.pop()

console.log("after pop")
console.log(colors)

console.log("after shift")
colors.shift()
console.log(colors)

console.log("after unshift")
colors.unshift("cyan")
console.log(colors)